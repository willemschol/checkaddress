export default async function handler(req, res) {
  try {
    const address = req.query.address;
    if (!address) {
      res.status(400).setHeader('Content-Type', 'text/plain');
      return res.send(' Falta parmetro address');
    }

    const geoRes = await fetch(
      `https://nominatim.openstreetmap.org/search?format=json&q=${encodeURIComponent(address)}`,
      {
        headers: {
          'User-Agent': 'CheckAddressApp/1.0 (contacto@tudominio.com)',
          'Accept': 'application/json'
        }
      }
    );
    if (!geoRes.ok) throw new Error(`Geocoding failed: ${geoRes.status}`);

    const geoJson = await geoRes.json();
    if (!Array.isArray(geoJson) || geoJson.length === 0) {
      res.status(200).setHeader('Content-Type', 'text/plain');
      return res.send(' Direccin no encontrada');
    }
    const { lat, lon } = geoJson[0];

    const area = coordsData[0];
    const coordinates = area.coordinates;
    const ring = coordinates.map(c => [parseFloat(c.Longitud), parseFloat(c.Latitud)]);
    if (ring.length < 3) throw new Error('Se requieren al menos 3 puntos');
    const [x0, y0] = ring[0];
    const [xn, yn] = ring[ring.length - 1];
    if (x0 !== xn || y0 !== yn) ring.push([x0, y0]);

    const turfPoly = polygon([ring]);
    const pt = point([parseFloat(lon), parseFloat(lat)]);
    const inside = booleanPointInPolygon(pt, turfPoly);

    res.status(200).setHeader('Content-Type', 'text/plain');
    return res.send(inside ? ' Dentro del rea' : ' Fuera del rea');

  } catch (err) {
    console.error('ERROR en /api/check:', err);
    res.status(500).setHeader('Content-Type', 'text/plain');
    return res.send(` Error interno en la funcin:\n${err.message}`);
  }
}
